package variable;

// 필수로 적어야 하는코드
public class variable
{
    public static void main(String[] args)
    {
        // 변수 value의 초기값을 10으로 설정
        int value = 10;

        // 변수 result의 초기값을 value + 10으로 설정
        int result = value + 10;

        // 변수 result를 출력
        System.out.println(result);
    }
}